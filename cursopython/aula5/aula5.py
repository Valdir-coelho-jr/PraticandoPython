"""
+, -, *, /, //, **, %, ()
+ - Soma
- - Subtração
* - Multiplicação
/ - Divisão
// - Divisão por inteiro
** - Exponenciação (Um numero elevado a outro)
% - Resto da divisão (retorna o resto da divisão de um numero e outro)
() - Alterar a precedencia das contas
"""

print('Multiplicação *', 10 * 10)
print('Adição +', 10 + 10)
print('Subtração -', 10 - 5)
print('Divisão /', 10 / 2)

# Divisão
print(7 / 2)

# Divisão por inteiro
print(7 // 2)

# Exponenciação
print(7 ** 7)

# Sobra da divisão
print(10 % 3)

# Precedência
print(5+2*10)
print((5+2)*10)


